type MessageFunction = (...args: never[]) => string

export type MessageShape<T> = {
  [K in keyof T]: T[K] extends (...args: infer A) => string
    ? (...args: A) => string
    : T[K] extends string
      ? string
      : T[K] extends MessageFunction
        ? T[K]
        : MessageShape<T[K]>
}

